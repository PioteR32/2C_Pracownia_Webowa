{
    document.write(number);
    switch(number)
    {
        case 1:
            document.write(" - Pierwsza");
            break;
        case 2:
            document.write(" - Druga");
            break;
        case 3:
            document.write(" - Trzecia");
            break;
        case 4:
            document.write(" - Czwarta");
            break;
        case 5:
            document.write(" - Piąta");
            break;
        case 6:
            document.write(" - Szósta");
            break;
        case 7:
            document.write(" - Siódma");
            break;
        case 8:
            document.write(" - Ósma");
            break;
        case 9:
            document.write(" - Dziewiąta");
            break;
        case 10:
            document.write(" - Dziesiąta");
            break;
    }
    document.write("<br>");

}